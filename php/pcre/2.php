     {3,5}表示3到5个字符。{3,}超过3个字符，{,5}最多5个，{3}三个字符。

     * 表示0到多个

     + 表示1到多个。


  ¤ 脱字符号

      ^:

          > 放在字符域(如:[^\w])中表示否定(不包括的意思)——“反向选择”

          >  放在表达式之前，表示以当前这个字符开始。(/^n/i,表示以n开头)。

      注意，我们经常管"\"叫"跳脱字符"。用于转义一些特殊符号，如".","/"
